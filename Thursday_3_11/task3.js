let mark = prompt('what is your mark ?');

if (mark >= 90) {
    document.write('A+');
}


else if (mark >= 80) {
    document.write('A');
}


else if (mark >= 70) {
    document.write('B');
}


else if (mark >=60) {
    document.write('C');
}


else if (mark >=50) {
    document.write('D');
}


else {
    document.write('FAIL');
}
